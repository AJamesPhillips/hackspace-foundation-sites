<h2>Memorandum of Association of Hackspace Foundation</h2>
<ol>
 <li>The name of the Company (hereinafter called "the Association") is Hackspace Foundation</li>
 <li>The registered office of the Association is to be situated in England and Wales</li>
 <li>The objects for which the Company is established are:
    <ol start="1">
        <li>To promote and support science, engineering, and art.</li>
    </ol>
 </li>
 <li>In support of the objects, but not otherwise, the Company shall have power to do all things incidental or conducive to the attainment of the objects or any of them. In particular (but without limitation) the Company shall have the following powers:
    <ol>
        <li>to pay out of the Company's funds the costs incurred in forming the Company;</li>
        <li>to pay out of the Company's funds premiums on insurance policies to cover the liability of the 
            members of the Board of Management established by the Articles of Association which by virtue of any rule of 
            law would otherwise attach to them in respect of any negligence, default, breach of duty or breach of trust 
            of which they may be guilty in relation to the Company: provided that any such insurance or indemnity shall 
            not extend to any claim arising from criminal neglect or deliberate default on their part;
        </li>
        <li>to invest and deal with the moneys of the Company not immediately required in such manner as may from time to 
            time be determined and to hold or otherwise deal with any investments made, provided that the Company shall not 
            have power to invest in any organisation which is a member of the Company at the time the investment is made;
        </li>
        <li>to promote any other company for the purpose of acquiring the whole or any part of the business or property 
            or undertaking or any of the liabilities of the Company, or of undertaking any business or operations which 
            may appear likely to assist or benefit the Company;
        </li>
        <li>to set up, establish, support and maintain superannuation and other funds or schemes (whether contributory or 
            non-contributory) for the benefit of any of the employees of the Company or of any subsidiary, holding or fellow 
            subsidiary of the Company and of their wives, widows, children and other relatives and dependants and to lend money 
            to any such employees or to trustees on their behalf to enable any such schemes to be established or maintained.
        </li>
    </ol>
  </li>
  <li>The income and capital of the Company shall be applied solely towards the promotion of the objects of the Company; and 
        no part of the income or capital shall be paid or transferred, directly or indirectly, to the members of the Company, 
        whether by way of dividend or bonus or otherwise in the form of profit. This shall not prevent the payment of:
    <ol>
        <li>reasonable and proper remuneration to any officer, employee, or member of the Company in return for any services 
            provided to the Company;
        </li>
        <li>a reasonable rate of interest on money lent to the Company;</li>
        <li>reasonable rent for property let to the Company;</li>
        <li>expenses to members of the Board of Management;</li>
        <li>premiums on the indemnity insurance referred to in clause 4.2.</li>
    </ol>
  </li>
  <li>The liability of the members is limited.</li>
  <li>If the Company is wound up while a person is a member or within one year after that person ceases to be a member, every 
        member of the Company will contribute such amount as may be required (not exceeding Â£5) to the assets of the Company, for 
        payment of the Company's debts and liabilities accrued before the member ceases to be a member, and of the costs and expenses 
        of winding up, and for the adjustment of the rights of the members among themselves.
  </li>
  <li>If on the winding up or dissolution of the Company there remains any surplus property after satisfaction of the Company's debts 
        and liabilities, the surplus shall not be paid to the members of the Company, but shall be either:
    <ol>
        <li>given or transferred to some other institution or institutions having objects similar to the objects of the Company; or</li>
        <li>If no such payment is possible, any surplus shall be applied to a charitable object. In each case, the recipient body or 
            bodies shall be chosen by the members as at the date of winding up or dissolution.
        </li>
    </ol>
</ol>
