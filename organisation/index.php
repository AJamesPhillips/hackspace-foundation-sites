<p>In order to successfully rent a space without one person taking on all the risk, an organisational structure is needed.
   We believe strongly in a transparent, community-run organisation.</p>

<p>The Hackspace Foundation is a registered company in England and Wales, limited by guarantee. A limited-by-guarantee 
   company has no shareholders to pay profit to. The founding documents of the Hackspace Foundation
   prevent paying profits to anyone, although we're not a registered charity and so still have to pay tax.</p>

<h3>Membership</h3>

<p>Anyone can become a member of the Hackspace Foundation by paying a monthly fee. Members of any of our 
   spaces are automatically members of the Foundation, but you can also become a standalone member.</p>

<p>Members of the Foundation are entitled to vote at meetings, and can elect directors.</p>

<h3>Directors</h3>

<p>The directors of the Foundation carry out the day-to-day business and are elected by the members yearly.
