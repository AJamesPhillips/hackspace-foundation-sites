<h3>Board of Management</h3>

<p>Until otherwise determined by a General Meeting, the number of members of the Board shall not be less than two nor more than eight.</p>

<p>The first members of the Board shall be as named in the Statement delivered to the Registrar of Companies pursuant to section 10 of the Act.</p>

<p>The Board may from time to time and at any time appoint any member of the Association as a member of the Board, either to fill a casual 
    vacancy or by way of addition to the Board, provided that the prescribed maximum be not thereby exceeded.  Any member so appointed 
    shall retain his office only until the next Annual General Meeting, but he shall then be eligible for re-election.
</p>

<p>No person who is not a member of the Association shall in any circumstances be eligible to hold office as a member of the Board.</p>

<h3>Powers of the Board</h3>

<p>The business of the Association shall be managed by the Board who may pay all such expenses of, and preliminary and incidental to, 
the promotion, formation, establishment and registration of the Association as they think fit, and may exercise all such powers of 
the Association, and do on behalf of the Association all such acts as may be exercised and done by the Association, and as are not 
by the Act or by the Articles required to be exercised or done by the Association in General Meeting, subject nevertheless to any 
regulations of the Articles, to the provisions of the Act and to such regulations, being not inconsistent with the aforesaid regulations 
or provisions, as may be prescribed by the Association in General Meeting; but no regulation made by the Association in General Meeting 
shall invalidate any prior act of the Board which would have been valid if such regulation had not been made.
</p>

<p>The members for the time being of the Board may act notwithstanding any vacancy in their body; provided always that in case the 
members of the Board shall at any time be or be reduced in number to less than the minimum number prescribed by or in accordance 
with the Articles, it shall be lawful for them to act as the Board for the purpose of admitting persons to membership of the Association, 
filling up vacancies in their body, or of summoning a General Meeting, but not for any other purpose.
</p>

<h3>Secretary</h3>
<p>Subject to the provisions of the Act the Secretary shall be appointed by the Board for such time at such remuneration and upon 
such conditions as they may think fit, and any Secretary so appointed may be removed by them.  The provisions of sections 283 and 
284 of the Act shall apply and be observed.  The Board may from time to time by resolution appoint an assistant or deputy Secretary, 
and any person so appointed may act in place of the Secretary if there be no Secretary or no Secretary capable of acting.
</p>

<h3>Disqualification of Members of the Board</h3>
<p>The office of a member of the Board shall be vacated -</p>
<ul>
    <li>If he becomes bankrupt or makes any arrangement or composition with his creditors generally.</li>
    <li>If he becomes of unsound mind.</li>
    <li>If he ceases to be a member of the Association.</li>
    <li>If by notice in writing to the Association he resigns his office.</li>
    <li>If he ceases to hold office by virtue of any provision of the Act or he becomes prohibited by law from being a Director of a Company.</li>
</ul>

<h3>Rotation of Members of the Board</h3>

<p>At the first Annual General Meeting and at any Annual General Meting to be held in any subsequent year, one-third of the 
members of the Board for the time being, or if their number is not a multiple of three then the number nearest to one-third, 
shall retire from office.</p>

<p>The members of the Board to retire shall be those who have been longest in office since their last election or appointment.  
As between members of equal seniority, the members to retire shall in the absence of agreement be selected from among them by lot. 
The length of time a member has been in office shall be computed from his last election or appointment.  A retiring member of the 
Board shall be eligible for re-election.</p>

<p>The Association may, at the meeting at which a member of the Board retires in manner aforesaid, fill up the vacated office by electing 
a person thereto, and in default the retiring member shall, if offering himself for re-election, be deemed to have been re-elected, 
unless at such meeting it is expressly resolved not to fill such vacated office, or unless a resolution for the re-election of such 
member shall have been put to the meeting and lost.</p>

<p>No person not being a member of the Board retiring at the meeting shall, unless recommended by the Board for election, be eligible 
for election to membership of the Board at any General Meeting, unless within the prescribed time before the day appointed for the meeting 
there shall have been given to the Secretary notice in writing, by some member duly qualified to be present and vote at the meeting for 
which such notice is given, of his intention to propose such person for election, and also notice in writing, signed by the person to 
be proposed, of his willingness to be elected.  The prescribed time above mentioned shall be such that, between the date when the notice 
is served, or deemed to be served, and the day appointed for the meeting there shall be not less than four nor more than 
twenty-eight intervening days.</p>

<p>The Association may from time to time in General Meeting increase or reduce the number of members of the Board, and determine in 
what rotation such increased or reduced number shall go out of office, and may make appointments necessary for effecting any such increase.</p>

<p>In addition and without prejudice to the provisions of section 303 of the Act, the Association may by Extraordinary Resolution remove 
any member of the Board before the expiration of his period of office, and may by an Ordinary Resolution appoint another qualified 
member in his stead; but any person so appointed shall retain his office so long as the member in whose place he is appointed would 
have held the same if he had not been removed.</p>

<h3>Proceedings of the Board</h3>
<p>The Board may meet together for the dispatch of business, adjourn and otherwise regulate their meetings as they think fit, and 
determine the quorum necessary for the transaction of business.  Unless otherwise determined, two shall be a quorum.  Questions arising 
at any meeting shall be decided by a majority of votes.  In case of an equality of votes the Chairman shall have a second or casting vote.</p>

<p>A member of the Board may, and on the request of a member of the Board the Secretary shall, at any time, summon a meeting of the Board 
by notice served upon the several members of the Board.  A member of the Board who is absent from the United Kingdom shall not be entitled 
to notice of a meeting.</p>

<p>The Board shall from time to time elect a Chairman who shall be entitled to preside at all meetings of the Board at which he shall be present, 
and may determine for what period he is to hold office, but if no such Chairman be elected, or if at any meeting the Chairman be not present 
within five minutes after the time appointed for holding the meeting and willing to preside, the members of the Board present shall choose 
one of their number to be Chairman of the meeting.</p>

<p>A meeting of the Board at which a quorum is present shall be competent to exercise all the authorities, powers and discretions by or 
under the regulations of the Association for the time being invested in the Board generally.</p>

<p>The Board may delegate any of their powers to committees consisting of such member or members of the Board as they think fit, and 
any committee so formed shall, in the exercise of the powers so delegated, conform to any regulations imposed on it by the Board.  The 
meetings and proceedings of any such committee shall be governed by the provisions of the Articles for regulating the meetings and proceedings 
of the Board so far as applicable and so far as the same shall not be superseded by any regulations made by the Board.</p>

<p>All acts bona fide done by any meeting of the Board or of any committee of the Board, or by any person acting as a member of the Board, 
shall, notwithstanding it be afterwards discovered that there was some defect in the appointment or continuance in office of any such 
member or person acting as aforesaid, or that they or any of them were disqualified, be as valid as if every such person had been duly 
appointed or had duly continued in office and was qualified to be a member of the Board.</p>

<p>The Board shall cause proper records to be kept of all Written Resolutions (and of the signatures).  The Board shall cause proper 
minutes to be made of all appointments of officers made by the Board and of the proceedings of all meetings of the Association and of 
the Board and of committees of the Board, and all business transacted at such meetings.  All such records (and signatures) and minutes 
shall be entered in books provided for the purpose. Any such record purporting to be signed by a Director or by the Secretary shall be 
evidence of the proceedings in agreeing to the Written Resolution and until the contrary is proved the requirements of the Act with respect 
to those proceedings shall be deemed to be complied with.  Any such minutes of any meeting, if purporting to be signed by the Chairman of 
such meeting, or by the Chairman of the next succeeding meeting, shall be sufficient evidence without any further proof of the facts therein stated.
</p>

<p>A resolution in writing signed by all the members for the time being of the Board or of any committee of the Board who are entitled to 
receive notice of a meeting of the Board or of such committee shall be as valid and effectual as if it had been passed at a meeting of 
the Board or of such committee duly convened and constituted.</p>

<h3>Accounts</h3>

<p>The Board shall cause accounting records to be kept in accordance with the requirements of the Act.</p>

<p>The accounting records shall be kept at the Office, or, subject to the provisions of the Act, at such other place or places as 
the Board shall think fit, and shall always be open to the inspection of the officers of the Association.</p>

<p>The Board shall from time to time determine whether and to what extent and at what times and places and under what conditions or 
regulations the accounting records of the Association or any of them shall be open to the inspection of members not being officers of the 
Association, and no member (not being an officer) shall have any right of inspecting any accounting records or other book or document of 
the Association except as conferred by statute or authorised by the Board or by the Association in General Meeting.
</p>

<p>The Board shall from time to time in accordance with the provisions of the Act cause to be prepared such income and expenditure 
accounts, balance sheets and reports as are required by the Act.  The Board shall send a copy of the annual accounts together with 
a copy of the Board’s report for that financial year and a copy of the Auditor’s report on those accounts to the Auditors and to every 
person entitled to receive the same in accordance with section 238 of the Act not less than 21 days before the date of the meeting at 
which those documents are to be laid in accordance with section 241 of the Act, or, where there is in force an election by Elective Resolution 
to dispense with the laying of accounts and report, not less than 28 days before the end of the period allowed for laying and delivering the same.
</p>

<h3>Audit</h3>

<p>In accordance with the provisions of the Act once at least in every year the accounts of the Association shall be examined and the 
correctness of the income and expenditure account and balance sheet ascertained by one or more properly qualified Auditor or Auditors.
</p>

<p>Auditors shall be appointed and their duties regulated in accordance with the provisions of the Act, the members of the Board being 
treated for all purposes as the Directors mentioned in those provisions.
</p>

<h3>Notices</h3>

<p>A notice may be served by the Association upon any member, either personally or by sending it through the post in a prepaid letter, 
addressed to such member at his registered address as appearing in the register of members.</p>

<p>Any member described in the register of members by an address not within the United Kingdom, who shall from time to time give the 
Association an address within the United Kingdom at which notices may be served upon him, shall be entitled to have notices served 
upon him at such address, but, save as aforesaid and as provided by the Act, only those members who are described in the register of 
members by an address within the United Kingdom shall be entitled to receive notices from the Association.
</p>

<p>Any notice, if served by post, shall be deemed to have been served on the day following that on which the letter containing the 
same is put into the post, and in proving such service it shall be sufficient to prove that the letter containing the notice was 
properly addressed and put into the post office as a prepaid first class letter.
</p>

<h3>Dissolution</h3>

<p>If upon the winding up or dissolution of the Association there remains, after the satisfaction of all its debts and liabilities, 
any property whatsoever, the same shall not be paid to or distributed among the members of the Association, but shall be transferred 
either to some other institution (whether or not a member of the Association) having objects similar to the objects of the Association, 
or to some institution (whether or not a member of the Association) the objects of which are the promotion of charity and anything 
incidental or conducive thereto, such institution or institutions to be determined by the members of the Association at or before the 
time of dissolution.
</p>

