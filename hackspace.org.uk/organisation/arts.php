<h2>Articles of Association of Hackspace Foundation</h2>
<h3>General</h3>
<p>In these presents the words standing in the first column of the Table next hereinafter contained 
   shall bear the meaning set opposite to them respectively in the second column thereof, if not inconsistent 
    with the subject or context -</p>
<dl>
    <dt>The Act</dt>
    <dd>The Companies Act 1985 and every statutory modification and re-enactment thereof for the time being in force.</dd>

    <dt>These Articles</dt>
    <dd>These Articles of Association, and the regulations of the Association from time to time in force.</dd>

    <dt>The Association</dt>
    <dd>The above-named Company.</dd>

    <dt>The Board</dt>
    <dd>The Board of Directors for the time being of the Association.</dd>

    <dt>The Office</dt>
    <dd>The registered office of the Association.</dd>

    <dt>The United Kingdom</dt>
    <dd>Great Britain and Northern Ireland</dd>

    <dt>Month</dt>
    <dd>Calendar month.</dd>

    <dt>In writing</dt>
    <dd>Written, printed or lithographed, or partly one partly another, and other modes of representing or producing words in a visible form.</dd>

    <dt>Clear days</dt>
    <dd>In relation to a period of notice means that period excluding the day when the notice is given or deemed to be 
        given and the day for which it is given or on which it is to take effect.</dd>
</dl>

<p>And words importing the singular number only shall include the plural number, and vice versa.</p>

<p>Words importing the masculine gender only shall include the feminine gender; and</p>

<p>Words importing persons shall include corporations.</p>

<p>Subject as aforesaid, any words or expressions defined in the Act shall, if not inconsistent with the 
    subject or context, bear the same meaning in the Articles.</p>

<p>The Association is established for the purposes expressed in the Memorandum of Association.</p>

<p>The subscribers to the Memorandum of Association and such other persons as the Board shall admit to membership 
    in accordance with the Articles shall be members of the Association.  No person shall be admitted as a member 
    of the Association unless he is approved by the Board.  Every person who wishes to become a member shall deliver 
    to the Association an application for membership in such form as the Board require executed by him.
</p>

<p>A member may at any time withdraw from the Association by giving at least seven days clear days’ notice to 
    the Association.  Membership shall not be transferable and shall cease on death.</p>

<h3>General Meetings</h3>
<p>Unless the Association has elected by Elective Resolution to dispense with the holding of Annual General Meetings 
    the Association shall hold a General Meeting in every calendar year as its Annual General Meeting at such time and 
    place as may be determined by the Board, and shall specify the meeting as such in the notices calling it, provided 
    that every Annual General Meeting except the first shall be held not more that fifteen months after the holding of 
    the last preceding Annual General Meeting, and that so long as the Association holds its first Annual General Meeting 
    within eighteen months after its incorporation it need not hold it in the year of its incorporation or in the following year.
    </p>

<p>All General Meetings, other than Annual General Meetings, shall be called Extraordinary General Meetings.</p>

<p>The Board may whenever they think fit convene an Extraordinary General Meeting, and Extraordinary General Meetings 
    shall also be convened on such requisition, or in default may be convened by such requisitionists, as provided by 
    section 368 of the Act.
    </p>

<p>Twenty-one clear days’ notice in writing at the least of every Annual General Meeting and of every meeting convened 
    to pass a Special Resolution or an Elective Resolution, and fourteen clear days’ notice in writing at the least of 
    every other General Meeting, specifying the place, the day and the hour of meeting, and in the case of special business 
    the general nature of that business, shall be given in manner hereinafter mentioned to such persons (including the Auditors) 
    as are under these Articles or under the Act entitled to receive such notices from the Association; but with the consent of 
    all the members having the right to attend and vote thereat, or of such proportion of them as is prescribed by the Act in the 
    case of meetings other than Annual General Meetings, a meeting may be convened by such notice as those members may think fit.
    </p>
<p>The accidental omission to give notice of a meeting to, or the non-receipt of such notice by, any person entitled to 
    receive notice thereof shall not invalidate any resolution passed, or proceeding had, at any meeting.
    </p>
<h3>Proceedings at General Meetings</h3>
<p>All business shall be deemed special that is transacted at an Extraordinary General Meeting, and all that is transacted 
    at an Annual General Meeting shall also be deemed special, with the exception of the consideration of the income and expenditure 
    account and balance sheet, and the reports of the Board and of the Auditors, the election of members of the Board in the place 
    of those retiring, and the appointment of, and the fixing of the remuneration of, the Auditors.
    </p>

<p>No business shall be transacted at any General Meeting unless a quorum is present when the meeting proceeds to business.  
    Save as herein otherwise provided three members personally present shall be a quorum.
</p>

<p>If within half an hour from the time appointed for the holding of a General Meeting a quorum is not present, the meeting, if 
    convened on the requisition of members, shall be dissolved.  In any other case it shall stand adjourned to the same day in 
    the next week, at the same time and place, or at such other place as the Board may determine.
</p>

<p>The Chairman (if any) of the Board shall preside as Chairman at every General Meeting, but if there be no such Chairman, or 
    if at any meeting he shall not be present within fifteen minutes after the time appointed for holding the same, or shall 
    be unwilling to preside, the members present shall choose some member of the Board, or if no such member be present, or 
    if all the members of the Board present decline to take the chair, they shall choose some member of the Association who 
    shall be present to preside.
</p>

<p>The Chairman may, with the consent of any meeting at which a quorum is present (and shall if so directed by the meeting) adjourn 
    the meeting from time to time, and from place to place, but no business shall be transacted at any adjourned meeting other than 
    business which might have been transacted at the meeting from which the adjournment took place.  Whenever a meeting is adjourned for 
    thirty days or more, notice of the adjourned meeting shall be given in the same manner as of an original meeting.  Save as aforesaid, 
    the members shall not be entitled to any notice of an adjournment, or of the business to be transacted at an adjourned meeting.
</p>

<p>At any General Meeting a resolution put to the vote of the meeting shall be decided on a show of hands, unless a poll is before or 
    upon the declaration of the result of the show of hands, demanded by the Chairman or by at least two members present in person or 
    by proxy, or by a member or members present in person or by proxy and representing one-tenth of the total voting rights of all 
    the members having the right to vote at the meeting, and unless a poll be so demanded a declaration by the Chairman of the meeting that 
    a resolution has been carried, or carried unanimously or by a particular majority, or lost, or not carried by a particular majority, and 
    an entry to that effect in the minute book of the Association shall be conclusive evidence of the fact without proof of the number or 
    proportion of the votes recorded in favour of or against that resolution.  The demand for a poll may be withdrawn.
</p>

<p>If a poll be demanded in manner aforesaid, it shall be taken at such time and place, and in such manner, as the Chairman of the meeting 
    shall direct, and the result of the poll shall be deemed to be the resolution of the meeting at which the poll was demanded.
</p>

<p>No poll shall be demanded on the election of a Chairman of a meeting, or on any question of adjournment.</p>

<p>In the case of an equality of votes, whether on a show of hands or on a poll, the Chairman of the meeting shall be entitled 
    to a second casting vote.</p>

<p>The demand of a poll shall not prevent the continuance of a meeting for the transaction of any business other than the question 
    on which a poll has been demanded.
</p>

<p>Subject to the provisions of the Act a resolution in writing signed by all the members for the time being entitled to receive 
    notice of and attend and vote at General Meetings (or being corporations by their duly authorised representatives) shall be as 
    valid and effective as if the same had been passed at a General Meeting of the Association duly convened and held.
</p>

<h3>Votes of Members</h3>
<p>Subject hereinafter provided, every member shall have one vote.</p>

<p>Save as herein expressly provided, no member other than a member duly registered, who shall have paid every subscription and 
    other sum (if any) which shall be due and payable to the Association in respect of his membership, shall be entitled to vote on 
    any question either personally or by proxy, or as proxy for another member, at any General Meeting.
</p>

<p>No objection shall be raised to the qualification of any voter except at the meeting or adjourned meeting at which the vote objected 
    to is tendered, and every vote not disallowed at the meeting shall be valid.  Any objection made in due time shall be referred to 
    the Chairman whose decision shall be final and conclusive.
</p>

<p>Votes may be given on a poll either personally or by proxy.  On a show of hands a member present only by proxy shall have no vote, 
    but a proxy for a corporation may vote on a show of hands.  A corporation may vote by its duly authorised representative appointed 
    as provided by section 375 of the Act.  A proxy need not be a member.
</p>

<p>The instrument appointing a proxy shall be in writing under the hand of the appointor or his attorney duly authorised in writing, 
    or if such appointor is a corporation under its common seal, if any, and, if none, then under the hand of some duty officer duly 
    authorised in that behalf.
</p>

<p>The instrument appointing a proxy and the power of attorney or other authority (if any) under which it is signed or a notarially 
    certified or office copy thereof shall be deposited at the Office not less than forty-eight hours before the time appointed for 
    holding the meeting or adjourned meeting at which the person named in the instrument proposes to vote, or in the case of a poll 
    not less than twenty-four hours before the time appointed for the taking of the poll, and in default the instrument of proxy shall 
    not be treated as valid.  No instrument appointing a proxy shall be valid after the expiration of twelve months from the date of 
    its execution.
</p>

<p>A vote given in accordance with the terms of an instrument of proxy shall be valid notwithstanding the previous death or insanity 
    of the principal or revocation of the proxy or of the authority under which the proxy was executed, provided that no intimation in 
    writing of the death, insanity or revocation as aforesaid shall have been received at the Office before the commencement of the 
    meeting or adjourned meeting at which the proxy is used.
</p>

